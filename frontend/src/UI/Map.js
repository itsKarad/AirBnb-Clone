const Map = (props) => {

    const mapRef = useRef();
    // eslint-disable-next-line
    const {center, zoom} = props;
    
    useEffect(() => {
        const map = new window.google.maps.Map(mapRef.current, {
            center: props.center,
            zoom: props.zoom,
            styles: MAP_STYLES,
        });
        new window.google.maps.Marker({
            position: props.center,
            map: map
        });        
    }, [center, zoom]);
    
    
    return (
        <div ref = {mapRef} className = {styles["map"]}>

        </div>
    );
};

